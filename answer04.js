// Q4: Usage of the Comma Operator in JavaScript

// The comma operator evaluates each of its operands (from left to right) 
// and returns the value of the last operand.

function commaOperatorExample() {
    let x = (1, 2, 3);
    return x; // Returns 3
}

// Example usage:
console.log(commaOperatorExample()); 
// Output: 3